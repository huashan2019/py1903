void	Refresh_BackCar_Trace(void)
{
	DWORD	adr;
	BYTE	rlc;
#if 1
	if( g_Group != G_TRACE )
		return;

	if( g_Trace_delay != 0 )
		return;
#endif
	g_Trace_delay = 200;											//30ms
	//g_TraceValue = 2;
	if( g_TraceValue == TRACK_MIDDLE_INDEX )								//Middle
	{
		adr = START_AT_Rle_UI_IB8_CarTrack_Middle;
		rlc = CP_IB8_CarTrack_Middle;		
	}
	else if( g_TraceValue > TRACK_MIDDLE_INDEX )
	{
		adr = BackCar_RightTrack[g_TraceValue - TRACK_MIDDLE_INDEX - 1].adr;
		rlc = BackCar_RightTrack[g_TraceValue - TRACK_MIDDLE_INDEX - 1].rlc;
	}
	else if( g_TraceValue < TRACK_MIDDLE_INDEX )
	{
		adr = BackCar_LeftTrack[TRACK_MIDDLE_INDEX - g_TraceValue - 1].adr;
		rlc = BackCar_LeftTrack[TRACK_MIDDLE_INDEX - g_TraceValue - 1].rlc;
	}

	Download_OSD_Lut(TRACE_WIN,TRACE_LUT,BG_LUT_OFFSET);
	Download_OSD_BMP_RLE(TRACE_WIN, 198, 310, 750, 350, adr+0x410,rlc, 0, 1);
	if( g_TraceValue >=  (TRACK_MIDDLE_INDEX + TRACK_CNT_RIGHT ) )
		g_TraceValue = 1;
	else
		g_TraceValue ++;

}


